package model;

/**
 *
 * @author TrungLT
 */
public class Employee {
    private int id;
    private String name;
    private Boolean gender;
    private String dob;
    private int salary;

    public Employee() {
    }

    public Employee(int id, String name, Boolean gender, String dob, int salary) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.dob = dob;
        this.salary = salary;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getGender() {
        return gender;
    }

    public void setGender(Boolean gender) {
        this.gender = gender;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }
}
