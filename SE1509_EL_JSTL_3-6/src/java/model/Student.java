package model;

/**
 *
 * @author TrungLT
 */
public class Student {
    private int id;
    private String name,dob,address;
    private double mark1,mark2;

    public Student() {
    }

    public Student(int id, String name, String dob, String address, double mark1, double mark2) {
        this.id = id;
        this.name = name;
        this.dob = dob;
        this.address = address;
        this.mark1 = mark1;
        this.mark2 = mark2;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public double getMark1() {
        return mark1;
    }

    public void setMark1(double mark1) {
        this.mark1 = mark1;
    }

    public double getMark2() {
        return mark2;
    }

    public void setMark2(double mark2) {
        this.mark2 = mark2;
    }  
}
