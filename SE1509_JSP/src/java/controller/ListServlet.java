    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String num_raw = request.getParameter("num");
        try {
            int num = Integer.parseInt(num_raw);
            List<Employee> list = new ArrayList<>();
            for (int i = 0; i < num; i++) {
                Random rd = new Random();
                // id from 10-99
                int id = rd.nextInt(99 + 1 - 10) - 10;

                //gender
                boolean gender = rd.nextBoolean();

                //name
                char[] nameArr = {'A', 'B', 'C', 'D', 'E', 'G', 'H'};
                String name;
                if (gender) {
                    name = "Mr. " + nameArr[rd.nextInt(nameArr.length)];
                } else {
                    name = "Ms. " + nameArr[rd.nextInt(nameArr.length)];
                }

                //mark
                int mark = rd.nextInt(11);

                Employee emp = new Employee(id, name, gender, mark);
                list.add(emp);
            }
            request.setAttribute("data", list);
            request.getRequestDispatcher("list.jsp").forward(request, response);
        } catch (NumberFormatException e) {
            System.out.println(e);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

